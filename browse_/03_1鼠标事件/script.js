
/**
 *
 *
 * 鼠标事件类型
 * mousedown/mouseup
 * 在元素上点击/释放鼠标按钮。
 * mouseover/mouseout
 * 鼠标指针从一个元素上移入/移出。
 * mousemove
 * 鼠标在元素上的每个移动都会触发此事件。
 * click
 * 如果使用的是鼠标左键，则在同一个元素上的 mousedown 及 mouseup 相继触发后，触发该事件。
 * dblclick
 * 在短时间内双击同一元素后触发。如今已经很少使用了。
 * contextmenu
 * 在鼠标右键被按下时触发。还有其他打开上下文菜单的方式，例如使用特殊的键盘按键，在这种情况下它也会被触发，因此它并不完全是鼠标事件。
 */


